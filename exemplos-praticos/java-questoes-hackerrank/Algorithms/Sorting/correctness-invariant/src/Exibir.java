public class Exibir {
    
    // Define um método estático chamado "exibir" que recebe um array de inteiros como argumento
    static void exibir(int[] arr) {

        // Itera sobre o array "arr" usando um loop "for-each" para cada elemento "i"
        for (int i : arr) {
            
            // Imprime o valor do elemento "i" seguido de um espaço em branco
            System.out.print(i + " ");
        }
    }
}
