
// Define uma classe 'Tracks' para representar uma faixa de trilhos com início e fim.
public class Tracks {
    
    long left;
    long right;

    // Construtor da classe 'Tracks' que inicializa o início e o fim da faixa.
    public Tracks(long l, long r) {

        this.left = l;
        this.right = r;
    }
}
